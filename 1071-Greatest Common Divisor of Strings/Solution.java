class Solution {
    public String gcdOfStrings(String str1, String str2) {
        //check mathmaticaly if the two words can have gcd 
        if (
            (Math.max(str1.length(),str2.length())%Math.min(str1.length(),str2.length())!=0)
        ){
            return "";
        }

        


        
        return"";
    }
}